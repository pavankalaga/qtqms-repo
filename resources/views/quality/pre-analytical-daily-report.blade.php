                document.getElementById('tableBody').appendChild(loadingRow);

                const params = new URLSearchParams();
                params.append('lab_id', labId);
                if (monthDate) {
                    params.append('month', monthDate);
                }

                const response = await fetch(`/api/get-quality-form-data?${params.toString()}`);
                const data = await response.json();

                loadingRow.remove();

                if (data.success) {
                    populateFormFields(data.data);
                } else {
                    console.error("Failed to fetch data:", data.message);
                }
            } catch (error) {
                console.error("Error fetching data:", error);
            }
        }

