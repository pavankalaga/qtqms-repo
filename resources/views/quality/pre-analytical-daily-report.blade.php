        function calculatePercentage(typeValue, totalValue) {
            // If total is empty or invalid, return "0%"
            if (totalValue === "" || isNaN(totalValue) || parseFloat(totalValue) === 0) {
                return "0%";
            }
            // If type is empty or invalid, treat as 0 (but still calculate)
            typeValue = (typeValue === "" || isNaN(typeValue)) ? 0 : parseFloat(typeValue);
            totalValue = parseFloat(totalValue);

            const percentage = (typeValue / totalValue) * 100;
            return percentage.toFixed(2) + "%";
        }

