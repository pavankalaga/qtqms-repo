        async function openNumeratorDetailsModal(rowId, day) {
            try {
                // Set modal title
                document.getElementById('modalDayNumber').textContent = day;
                document.getElementById('modalRowId').value = rowId;
                document.getElementById('modalDay').value = day;

                // Clear form and show loading state
                document.getElementById('numeratorDetailsForm').reset();
                const detailsList = document.getElementById('detailsList');
                detailsList.innerHTML = '<div class="text-center py-3">Loading details...</div>';

                // Try to fetch existing details
                try {
                    const response = await fetch(`/get-numerator-details?row_id=${rowId}&day=${day}`);

                    // Check if response is HTML (404 page) instead of JSON
                    const contentType = response.headers.get('content-type');
                    if (!contentType || !contentType.includes('application/json')) {
                        throw new Error('Invalid response format');
                    }

                    const data = await response.json();

                    if (data.success) {
                        if (data.data && data.data.length > 0) {
                            // Display existing details
                            renderDetailsList(data.data);

                            // Auto-fill form with latest entry
                            const latestEntry = data.data[data.data.length - 1];
                            document.getElementById('barcode').value = latestEntry.barcode || '';
                            document.getElementById('lis_ticket').value = latestEntry.lis_ticket_number || '';
                        } else {
                            detailsList.innerHTML = '<div class="alert alert-info">No details found for this day.</div>';
                        }
                    } else {
                        detailsList.innerHTML = `<div class="alert alert-warning">${data.message || 'Error loading details'}</div>`;
                    }
                } catch (error) {
                    console.error('Error fetching details:', error);
                    detailsList.innerHTML = '<div class="alert alert-info">No existing details found. You can add new ones below.</div>';
                }

                // Show modal
                const modal = document.getElementById('numeratorDetailsModal');
                modal.style.display = 'block';
                modal.classList.add('show');

            } catch (error) {
                console.error('Error in openNumeratorDetailsModal:', error);
                alert('Error opening details: ' + error.message);
            }
        }

        function renderDetailsList(details) {
            const detailsList = document.getElementById('detailsList');
            detailsList.innerHTML = '';

            const listGroup = document.createElement('div');
            listGroup.className = 'list-group mb-3';

            // Sort by ID descending to show latest first
            details.sort((a, b) => b.id - a.id).forEach(detail => {
                const item = document.createElement('div');
                item.className = 'list-group-item';
                item.innerHTML = `
                                <div class="d-flex justify-content-between align-items-center">
                                    <div>
                                        <strong>Barcode:</strong> ${detail.barcode || 'N/A'}<br>
                                        <strong>LIS Ticket:</strong> ${detail.lis_ticket_number || 'N/A'}
                                    </div>
                                    <div>
                                        <button class="btn btn-sm btn-primary edit-detail mr-2" data-id="${detail.id}">
                                            <i class="fas fa-edit"></i>
                                        </button>
                                        <button class="btn btn-sm btn-danger delete-detail" data-id="${detail.id}">
                                            <i class="fas fa-trash"></i>
                                        </button>
                                    </div>
                                </div>
                                <small class="text-muted">Added: ${new Date(detail.created_at).toLocaleString()}</small>
                            `;
                listGroup.appendChild(item);
            });

            detailsList.appendChild(listGroup);

            // Add edit handlers
            document.querySelectorAll('.edit-detail').forEach(btn => {
                btn.addEventListener('click', async (e) => {
                    e.preventDefault();
                    const detailId = btn.dataset.id;
                    const detail = details.find(d => d.id == detailId);
                    if (detail) {
                        document.getElementById('barcode').value = detail.barcode || '';
                        document.getElementById('lis_ticket').value = detail.lis_ticket_number || '';
                        // Scroll to form
                        document.getElementById('numeratorDetailsForm').scrollIntoView({ behavior: 'smooth' });
                    }
                });
            });

            // Add delete handlers
            document.querySelectorAll('.delete-detail').forEach(btn => {
                btn.addEventListener('click', async (e) => {
                    e.preventDefault();
                    if (confirm('Are you sure you want to delete this detail?')) {
                        await deleteNumeratorDetail(btn.dataset.id);
                        await openNumeratorDetailsModal(rowId, day); // Refresh
                    }
                });
            });
        }



        async function deleteNumeratorDetail(id) {
            try {
                const response = await fetch(`/delete-numerator-detail/${id}`, {
                    method: 'DELETE',
                    headers: {
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content,
                        'Content-Type': 'application/json'
                    }
                });

                const data = await response.json();
                if (!data.success) {
                    throw new Error(data.message);
                }

                return data;
            } catch (error) {
                console.error('Error deleting detail:', error);
                throw error;
            }
        }

