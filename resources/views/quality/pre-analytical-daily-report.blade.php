        // Function to collect form data
        function collectFormData() {
            const formData = [];

            tableData.rows.forEach(row => {
                let totalMisidentified = 0;
                let totalSamples = 0;

                const rowData = {
                    id: row.id,
                    title: row.title,
                    subtitle: row.subtitle,
                    days: [],
                    average: document.getElementById(`row${row.id}_average`).textContent.replace('%', ''),
                    total_misidentified: 0, // Initialize totals
                    total_samples: 0
                };

                for (let day = 1; day <= 31; day++) {
                    const typeValue = document.querySelector(`input[name="row${row.id}_day${day}_type"]`)?.value || 0;
                    const totalValue = document.querySelector(`input[name="row${row.id}_day${day}_total"]`)?.value || 0;
                    const percentage = document.getElementById(`row${row.id}_day${day}_percent`)?.textContent.replace('%', '') || '0';

                    // Calculate totals
                    totalMisidentified += parseFloat(typeValue) || 0;
                    totalSamples += parseFloat(totalValue) || 0;

                    rowData.days.push({
                        day: day,
                        type: typeValue,
                        total: totalValue,
                        percentage: percentage
                    });
                }

                // Add totals to row data
                rowData.total_misidentified = totalMisidentified.toFixed(2);
                rowData.total_samples = totalSamples.toFixed(2);

                formData.push(rowData);
            });

            return formData;
        }


